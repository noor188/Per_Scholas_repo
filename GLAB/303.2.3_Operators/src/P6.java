public class P6 {
    public static void main(String[] args) {
        int x = 1;
        System.out.println("x :" + x);
        x = ++x;
        System.out.println("x+1 :" + x);
        x = x+1;
        System.out.println("x+1 :" + x);
        x = (x >> 1) << 2;
        System.out.println("x+1 :" + x);

    }
}
